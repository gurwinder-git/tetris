export const random = (min, max) => {
    return Math.trunc(Math.random() * (max - min + 1)) + min
}

export const defaultGrid = () => {
    const rows = 18
    const cols = 10
    const grid = []

    for (let row = 0; row < rows; row++) {
        grid.push([])
        for (let col = 0; col < cols; col++) {
            grid[row].push(0)
        }
    }

    return grid
}

